// Permutation table for bit mixing
static const uint8_t PERM_TABLE[16] = {
    0, 13, 10, 7, 4, 1, 14, 11, 8, 5, 2, 15, 12, 9, 6, 3
};

// Round key generation from master key
static void generate_round_keys(const uint8_t *key, uint8_t round_keys[10][16]) {
    // Simple key schedule: XOR key with round constants
    for (int round = 0; round < 10; round++) {
        for (int i = 0; i < 16; i++) {
            uint8_t key_byte = key[i % KEY_SIZE];
            round_keys[round][i] = key_byte ^ (round * 17 + i * 3) ^ SBOX[(round + i) % 256];
        }
    }
}

// Substitution layer
static void substitute_bytes(uint8_t *state, const uint8_t *sbox) {
    for (int i = 0; i < BLOCK_SIZE; i++) {
        state[i] = sbox[state[i]];
    }
}

// Permutation layer
static void permute_bytes(uint8_t *state) {
    uint8_t temp[BLOCK_SIZE];
    memcpy(temp, state, BLOCK_SIZE);
    for (int i = 0; i < BLOCK_SIZE; i++) {
        state[i] = temp[PERM_TABLE[i]];
    }
}

// Add round key
static void add_round_key(uint8_t *state, const uint8_t *round_key) {
    for (int i = 0; i < BLOCK_SIZE; i++) {
        state[i] ^= round_key[i];
    }
}

// Mix columns (simple diffusion)
static void mix_columns(uint8_t *state) {
    for (int i = 0; i < 4; i++) {
        uint8_t a = state[i * 4];
        uint8_t b = state[i * 4 + 1];
        uint8_t c = state[i * 4 + 2];
        uint8_t d = state[i * 4 + 3];
        
        state[i * 4]     = a ^ b ^ c;
        state[i * 4 + 1] = b ^ c ^ d;
        state[i * 4 + 2] = c ^ d ^ a;
        state[i * 4 + 3] = d ^ a ^ b;
    }
}

// Inverse mix columns
static void inv_mix_columns(uint8_t *state) {
    for (int i = 0; i < 4; i++) {
        uint8_t a = state[i * 4];
        uint8_t b = state[i * 4 + 1];
        uint8_t c = state[i * 4 + 2];
        uint8_t d = state[i * 4 + 3];
        
        state[i * 4]     = b ^ c ^ d;
        state[i * 4 + 1] = a ^ c ^ d;
        state[i * 4 + 2] = a ^ b ^ d;
        state[i * 4 + 3] = a ^ b ^ c;
    }
}

// Encrypt a single block
void cipher_encrypt_block(const uint8_t *input, uint8_t *output, const uint8_t *key) {
    uint8_t state[BLOCK_SIZE];
    uint8_t round_keys[10][16];
    
    memcpy(state, input, BLOCK_SIZE);
    generate_round_keys(key, round_keys);
    
    // Initial round key addition
    add_round_key(state, round_keys[0]);
    
    // 9 main rounds
    for (int round = 1; round < 10; round++) {
        substitute_bytes(state, SBOX);
        permute_bytes(state);
        mix_columns(state);
        add_round_key(state, round_keys[round]);
    }
    
    // Final round (no mix columns)
    substitute_bytes(state, SBOX);
    permute_bytes(state);
    
    memcpy(output, state, BLOCK_SIZE);
}

// Decrypt a single block
void cipher_decrypt_block(const uint8_t *input, uint8_t *output, const uint8_t *key) {
    uint8_t state[BLOCK_SIZE];
    uint8_t round_keys[10][16];
    
    memcpy(state, input, BLOCK_SIZE);
    generate_round_keys(key, round_keys);
    
    // Reverse final round
    permute_bytes(state);
    substitute_bytes(state, INV_SBOX);
    
    // 9 main rounds in reverse
    for (int round = 9; round >= 1; round--) {
        add_round_key(state, round_keys[round]);
        inv_mix_columns(state);
        permute_bytes(state);
        substitute_bytes(state, INV_SBOX);
    }
    
    // Initial round key removal
    add_round_key(state, round_keys[0]);
    
    memcpy(output, state, BLOCK_SIZE);
}

// CBC mode encryption
int cbc_encrypt(const uint8_t *plaintext, size_t plaintext_len,
                const uint8_t *key, const uint8_t *iv,
                uint8_t **ciphertext, size_t *ciphertext_len) {
    
    // Calculate padded length
    size_t padded_len = plaintext_len + (BLOCK_SIZE - (plaintext_len % BLOCK_SIZE));
    
    // Allocate memory for padded plaintext
    uint8_t *padded_plaintext = malloc(padded_len);
    if (!padded_plaintext) {
        return -1;
    }
    
    memcpy(padded_plaintext, plaintext, plaintext_len);
    add_pkcs7_padding(padded_plaintext + plaintext_len, plaintext_len, BLOCK_SIZE);
    
    // Allocate ciphertext
    *ciphertext = malloc(padded_len);
    if (!*ciphertext) {
        free(padded_plaintext);
        return -1;
    }
    
    *ciphertext_len = padded_len;
    
    // CBC encryption
    uint8_t prev_block[BLOCK_SIZE];
    memcpy(prev_block, iv, BLOCK_SIZE);
    
    for (size_t i = 0; i < padded_len; i += BLOCK_SIZE) {
        uint8_t block[BLOCK_SIZE];
        
        // XOR with previous ciphertext block (or IV)
        for (int j = 0; j < BLOCK_SIZE; j++) {
            block[j] = padded_plaintext[i + j] ^ prev_block[j];
        }
        
        // Encrypt block
        cipher_encrypt_block(block, *ciphertext + i, key);
        
        // Update previous block
        memcpy(prev_block, *ciphertext + i, BLOCK_SIZE);
    }
    
    free(padded_plaintext);
    return 0;
}

// CBC mode decryption
int cbc_decrypt(const uint8_t *ciphertext, size_t ciphertext_len,
                const uint8_t *key, const uint8_t *iv,
                uint8_t **plaintext, size_t *plaintext_len) {
    
    if (ciphertext_len % BLOCK_SIZE != 0) {
        return -1;
    }
    
    // Allocate plaintext buffer
    uint8_t *padded_plaintext = malloc(ciphertext_len);
    if (!padded_plaintext) {
        return -1;
    }
    
    // CBC decryption
    uint8_t prev_block[BLOCK_SIZE];
    memcpy(prev_block, iv, BLOCK_SIZE);
    
    for (size_t i = 0; i < ciphertext_len; i += BLOCK_SIZE) {
        uint8_t block[BLOCK_SIZE];
        
        // Decrypt block
        cipher_decrypt_block(ciphertext + i, block, key);
        
        // XOR with previous ciphertext block (or IV)
        for (int j = 0; j < BLOCK_SIZE; j++) {
            padded_plaintext[i + j] = block[j] ^ prev_block[j];
        }
        
        // Update previous block
        memcpy(prev_block, ciphertext + i, BLOCK_SIZE);
    }
    
    // Remove padding
    size_t unpadded_len;
    if (remove_pkcs7_padding(padded_plaintext, ciphertext_len, &unpadded_len) != 0) {
        free(padded_plaintext);
        return -1;
    }
    
    *plaintext = malloc(unpadded_len);
    if (!*plaintext) {
        free(padded_plaintext);
        return -1;
    }
    
    memcpy(*plaintext, padded_plaintext, unpadded_len);
    *plaintext_len = unpadded_len;
    
    free(padded_plaintext);
    return 0;
}
